function myHid() {
  var a = document.getElementById("Hide");

  if (a.style.display === "none") {
    a.style.display = "block";
    document.getElementById("readmore").innerHTML = "-";
  } else {
    a.style.display = "none";
    document.getElementById("readmore").innerHTML = "+";
  }
}

